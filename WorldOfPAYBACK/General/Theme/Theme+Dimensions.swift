import Foundation

class Theme {
    enum Dimensions {
        /// Zero margin: 0 px
        static let marginZero: CGFloat = 0

        /// Small margin: 1 px
        static let marginMinimal: CGFloat = 1.0

        /// Small margin: 2 px
        static let marginSmall: CGFloat = 2.0

        /// Extra extra small margin: 4 px
        static let marginExtraExtraSmall: CGFloat = 4

        /// Extra small margin: 5 px
        static let marginExtraSmall: CGFloat = 5

        /// Default margin: 8 px
        static let defaultLayoutMargin: CGFloat = 8

        /// Small vertical margin: 10 px
        static let marginSmallVertical: CGFloat = 10

        /// Small horizontal margin: 15 px
        static let marginSmallHorizontal: CGFloat = 12

        /// Medium horizontal margin: 15 px
        static let marginMediumHorizontal: CGFloat = 15

        /// Medium vertical margin: 16 px
        static let marginMediumVertical: CGFloat = 16

        /// Medium margin: 17 px
        static let marginMedium: CGFloat = 17

        /// Medium plus margin: 20 px
        static let marginMediumPlus: CGFloat = 20

        /// Semi medium margin: 24 px
        static let marginSemiMedium: CGFloat = 24

        /// Extra medium margin: 25 px
        static let marginExtraMedium: CGFloat = 25

        /// Semi large margin: 28 px
        static let marginSemiLarge: CGFloat = 28

        /// Large margin: 32 px
        static let marginLarge: CGFloat = 32

        /// Semi extra large margin: 40 px
        static let marginSemiExtraLarge: CGFloat = 40

        /// Extra  large margin: 48 px
        static let marginExtraLarge: CGFloat = 48

        /// Extra Extra large margin: 60 px
        static let marginExtraExtraLarge: CGFloat = 60

        /// Super large margin: 60 px
        static let marginSuperLarge: CGFloat = 70

        /// Default iPad margin: 200 px
        static let marginDefaultIPad: CGFloat = 200
    }
}
